/// Spacing constants on the 8-pt grid.
///
/// Use these for all padding, margin, and SizedBox values.
/// Off-grid values (6, 10, 12, 20, 28…) stay as numeric literals
/// with an `// off-grid` comment.
class AppSpacing {
  AppSpacing._();

  /// 4 px
  static const double xs = 4;

  /// 8 px
  static const double sm = 8;

  /// 16 px
  static const double md = 16;

  /// 24 px
  static const double lg = 24;

  /// 32 px
  static const double xl = 32;

  /// 48 px
  static const double xxl = 48;
}
